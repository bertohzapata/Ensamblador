ASSUME CS:CODIGO, DS:DATOS, SS:PILA

;CALCULAR LA SIGUIENTE SUMATORIA: 0+2+4+6+...+2N

CODIGO SEGMENT	
	MOV AX,DATOS
	MOV DS,AX
	
	MOV N, 150	;EL VALOR DE N DETERMINA LAS VECES QUE SE EJECUTA EL CICLO, EN ESTE CASO 2*150=300
	MOV DL, N
ETQ2:	
	CMP I,DL
	JA ETQ1
	MOV AL, 2
	MOV BL, I
	MUL BL
	MOV RES, AX
	INC I
	JMP ETQ2
ETQ1:
	MOV AH,4Ch  
	INT 21h         
CODIGO ENDS

PILA SEGMENT STACK
	DW 32 DUP (0)
PILA ENDS

DATOS SEGMENT
;SE INICIALIZAN LAS VARIABLES EN CERO:
N DB ?
RES DW 0
I DB 0
DATOS ENDS

END
