ASSUME CS:CODIGO, DS:DATOS, SS:PILA

;IMPRIMIR DE FORMA INVERTIDA, EN PANTALLA, UNA CADENA DE CARACTERES INGRESADA POR TECLADO (USAR LA PILA DEL PROGRAMA)

CODIGO SEGMENT	
	MOV AX,DATOS
	MOV DS,AX
	
ET1:
	MOV AH, 01H
	INT 21H
	CMP AL, 0DH
	JE ET2
	PUSH AX
	INC K
	JMP ET1
ET2:
	CMP K, 0H
	JE FIN
	POP BX
	MOV DL, BL
	MOV AH, 2H
	INT 21H
	DEC K
	JMP ET2
FIN:
	MOV AH,4Ch  
	INT 21h         
CODIGO ENDS

PILA SEGMENT STACK
	DW 32 DUP (0)
PILA ENDS

DATOS SEGMENT
K DB 0H
DATOS ENDS

END
